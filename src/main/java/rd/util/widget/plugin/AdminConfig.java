package rd.util.widget.plugin;

public class AdminConfig {

	private String menu;
	private String tabName;
	private String tabFunction;
	
	public AdminConfig(){}
	
	public String getMenu() {
		return menu;
	}
	public void setMenu(String menu) {
		this.menu = menu;
	}
	public String getTabName() {
		return tabName;
	}
	public void setTabName(String tabName) {
		this.tabName = tabName;
	}
	public String getTabFunction() {
		return tabFunction;
	}
	public void setTabFunction(String tabFunction) {
		this.tabFunction = tabFunction;
	}
}
